#include "../includes/ft_printf.h"

int	ftp_putstr(char *s)
{
	int	i;

	if (!s)
		return (ftp_putstr("(null)"));
	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i);
}
